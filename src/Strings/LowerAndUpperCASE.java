package Strings;

public class LowerAndUpperCASE {

	public static void main(String[] args) {

		// TO UPPER CASE
		String text = "All Kind OF LeTTeRs";
		System.out.println(text.toUpperCase());

		// to lower case
		String text1 = "All Kind OF LeTTeRs";
		System.out.println(text1.toLowerCase());

	}
}
